#include <iostream>
using namespace std;

void display() {
    double a = 1.5;
    double b = -2;
    int x = 20;
    int y = 25;
    cout << "a = " << a << ", b = " << b << endl << "x = " << x << ", y = " << y << endl;
}

int main() {
    int a = 5, b = 10;
    int x = 15;
    int y = 10;
    display();
    cout << "a = " << a << ", b = " << b << endl << "x = " << x << ", y = " << y << endl;
    return 0;
}

